def build_ast_symbol_table(written_files: dict) -> dict:
    """Build a symbol table from all written files for cross-file awareness.

    Returns:
    {
        "functions": {"function_name": "defined_in_file.py"},
        "classes": {"ClassName": "defined_in_file.py"},
        "endpoints": {"/api/path": {"method": "POST", "defined_in": "routes.py"}},
        "exports": {"export_name": "defined_in_file.js"}
    }
    """
    table: dict[str, dict] = {
        "functions": {},
        "classes": {},
        "endpoints": {},
        "exports": {},
    }

    for fpath, content in written_files.items():
        ext = os.path.splitext(fpath)[1].lower()

        if ext == ".py":
            _extract_python_symbols(fpath, content, table)
        elif ext in (".js", ".mjs", ".jsx", ".ts", ".tsx", ".mts"):
            _extract_js_symbols(fpath, content, table)

    return table


def _extract_python_symbols(fpath: str, content: str, table: dict) -> None:
    """Extract Python symbols using AST."""
    try:
        tree = ast.parse(content)
    except SyntaxError:
        return

    for node in ast.walk(tree):
        if isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef)):
            table["functions"][node.name] = fpath

            # Check for route decorators
            for dec in node.decorator_list:
                route_info = _extract_fastapi_route(dec)
                if route_info:
                    path, method = route_info
                    table["endpoints"][path] = {
                        "method": method,
                        "defined_in": fpath,
                    }

        elif isinstance(node, ast.ClassDef):
            table["classes"][node.name] = fpath


def _extract_js_symbols(fpath: str, content: str, table: dict) -> None:
    """Extract JavaScript/TypeScript symbols using regex."""
    # Functions
    for match in re.finditer(r'(?:function|async\s+function)\s+(\w+)\s*\(', content):
        table["functions"][match.group(1)] = fpath

    # Arrow functions assigned to const
    for match in re.finditer(r'(?:const|let|var)\s+(\w+)\s*=\s*(?:async\s*)?\(', content):
        table["functions"][match.group(1)] = fpath

    # Classes
    for match in re.finditer(r'class\s+(\w+)', content):
        table["classes"][match.group(1)] = fpath

    # Exports
    for match in re.finditer(r'export\s+(?:default\s+)?(?:function|class|const|let|var)\s+(\w+)', content):
        table["exports"][match.group(1)] = fpath

    # module.exports
    for match in re.finditer(r'module\.exports\s*=\s*\{([^}]+)\}', content):
        for name in re.findall(r'(\w+)', match.group(1)):
            table["exports"][name] = fpath

    # Express/router routes
    for match in re.finditer(
        r'''(?:router|app)\.(get|post|put|delete|patch)\s*\(\s*['"]([^'"]+)['"]''',
        content, re.IGNORECASE,
    ):
        method = match.group(1).upper()
        path = match.group(2)
        table["endpoints"][path] = {"method": method, "defined_in": fpath}


def format_symbol_table_for_prompt(symbol_table: dict) -> str:
    """Format the symbol table as context for the Coder LLM."""
    if not any(symbol_table.values()):
        return ""

    lines = ["=== ALREADY DEFINED (DO NOT REDEFINE) ==="]

    if symbol_table.get("endpoints"):
        lines.append("\nAPI Endpoints:")
        for path, info in symbol_table["endpoints"].items():
            lines.append(f"  {info['method']} {path} (in {info['defined_in']})")

    if symbol_table.get("functions"):
        lines.append("\nFunctions:")
        for name, fpath in list(symbol_table["functions"].items())[:30]:
            lines.append(f"  {name}() — defined in {fpath}")

    if symbol_table.get("classes"):
        lines.append("\nClasses:")
        for name, fpath in symbol_table["classes"].items():
            lines.append(f"  {name} — defined in {fpath}")

    if symbol_table.get("exports"):
        lines.append("\nExports:")
        for name, fpath in list(symbol_table["exports"].items())[:20]:
            lines.append(f"  {name} — from {fpath}")

    return "\n".join(lines)


# ---------------------------------------------------------------------------
# Path matching helpers
# ---------------------------------------------------------------------------
def _paths_match(spec_path: str, code_path: str) -> bool:
    """Check if two URL paths match, treating {param} as wildcards."""
    spec_parts = spec_path.strip("/").split("/")
    code_parts = code_path.strip("/").split("/")

    if len(spec_parts) != len(code_parts):
        return False

    for sp, cp in zip(spec_parts, code_parts):
        # Path params
        if sp.startswith("{") and sp.endswith("}"):
            continue
        if cp.startswith("{") and cp.endswith("}"):
            continue
        if cp.startswith(":"):
            continue  # Express-style :param
        if sp.lower() != cp.lower():
            return False

    return True


def _find_similar_path(target: str, candidates: list[str]) -> str | None:
    """Find the most similar path from candidates using simple heuristics."""
    target_parts = target.strip("/").split("/")

    best_match = None
    best_score = 0

    for candidate in candidates:
        cand_parts = candidate.strip("/").split("/")

        # Count matching segments
        score = 0
        min_len = min(len(target_parts), len(cand_parts))
        for i in range(min_len):
            tp = target_parts[i]
            cp = cand_parts[i]
            if tp == cp:
                score += 2
            elif tp.lower() == cp.lower():
                score += 1
            elif tp.startswith("{") or cp.startswith("{") or cp.startswith(":"):
                score += 1

        # Penalize length mismatch
        score -= abs(len(target_parts) - len(cand_parts))

        if score > best_score:
            best_score = score
            best_match = candidate

    # Only return if score is above threshold
    if best_score >= len(target_parts):
        return best_match
    return None


def _find_matching_spec_path(url: str, spec_paths: dict[str, str]) -> str | None:
    """Find the matching spec path for a URL."""
    for spec_path in spec_paths:
        if _paths_match(spec_path, url):
            return spec_path
    return None


def _to_camel_case(name: str) -> str:
    """Convert snake_case to camelCase."""
    parts = name.split("_")
    return parts[0] + "".join(p.capitalize() for p in parts[1:])


def _to_snake_case(name: str) -> str:
    """Convert camelCase to snake_case."""
    result = re.sub(r'([A-Z])', r'_\1', name).lower()
    return result.lstrip("_")
